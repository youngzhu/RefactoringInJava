package com.youngzy.refactoring.edition2.ch06.tp08.v01;

public class NumberRange {
    private double min;
    private double max;

    public NumberRange(double min, double max) {
        this.min = min;
        this.max = max;
    }

    public double getMin() {
        return min;
    }

    public double getMax() {
        return max;
    }
}
